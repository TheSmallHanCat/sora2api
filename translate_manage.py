def translate_file(input_file, output_file=None):
    """Переводит китайские тексты в файле"""
    if output_file is None:
        output_file = input_file
    
    with open(input_file, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Применяем переводы
    for chinese, russian in translations.items():
        content = content.replace(chinese, russian)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print(f"✅ Перевод завершен: {output_file}")

